package Model;

public class Superlative {
    private double a;
    private double b;
    private double x;

    public Superlative() {
    }

    public Superlative(double a, double b, double x) {
        this.a = a;
        this.b = b;
        this.x = x;
    }

    public double getA() {
        return a;
    }

    public void setA(double a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }
}
